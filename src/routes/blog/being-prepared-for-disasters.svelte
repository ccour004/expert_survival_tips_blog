<p>Emergency preparation is very important and this includes being prepared for disasters. While no one likes to think that something bad will happen, the truth is that sometimes it does and you and your family will be much better off if you are prepared for this before it occurs.</p>
<p>So how do you go about being prepared for disasters? Here are some ideas:</p>
<ul>
<li>Be aware of potential disasters that can occur in your area</li>
<li>Prepare your home for natural disasters and other problems</li>
<li>Have an emergency escape plan such as fire exits and other plants</li>
<li>Practice fire drills and family emergency escape plans</li>
<li>Plan for where you will go in the event that you need to evacuate your area</li>
</ul>
<p>If the event of a disaster, does your family know what to do? Do you have a plan for if you get separated? Where will you go? Do your children know who to call if something happens to the adults? These are all scary things to think about happening but important things to prepare for. As is often said, itâ€™s much better to be prepared and never have to do it than to have a disaster occur and not be ready.</p>
